	/*
	* Save the Activation Dates for PMS (appraisal and review)
	*/
	
	public function save(PmsDates $dateObject)
	{
		$dateData = $this->hydrator->extract($dateObject);
		unset($dateData['id']);
		
		$dateData['start_Date'] = date("Y-m-d", strtotime(substr($dateData['date_Range'],0,10)));
		$dateData['end_Date'] = date("Y-m-d", strtotime(substr($dateData['date_Range'],13,10)));
		
		unset($dateData['date_Range']);
		
		if($dateObject->getId()) {
			//ID present, so it is an update
			$action = new Update('pms_activation_dates');
			$action->set($dateData);
			$action->where(array('id = ?' => $dateObject->getId()));
		} else {
			//ID is not present, so its an insert
			$action = new Insert('pms_activation_dates');
			$action->values($dateData);
		}
		
		$sql = new Sql($this->dbAdapter);
		$stmt = $sql->prepareStatementForSqlObject($action);
		$result = $stmt->execute();

		
		if($result instanceof ResultInterface) {
			if($newId = $result->getGeneratedValue()){
				//when a value has been generated, set it on the object
				$dateObject->setId($newId);
			}
			return $dateObject;
		}
		
		throw new \Exception("Database Error");
	}
	
