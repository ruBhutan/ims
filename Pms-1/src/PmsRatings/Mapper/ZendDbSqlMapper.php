	}
        
	/**
	 * 
	 * @param type $PmsRatingsInterface
	 * 
	 * to save Vision and Mission Details
	 */
	
	public function saveQuestion(FeedbackQuestions $pmsObject, $tableName)
	{
		$pmsData = $this->hydrator->extract($pmsObject);
		unset($pmsData['id']);
		
		if($pmsObject->getId()) {
			//ID present, so it is an update
			$action = new Update($tableName);
			$action->set($pmsData);
			$action->where(array('id = ?' => $pmsObject->getId()));
		} else {
			//ID is not present, so its an insert
			$action = new Insert($tableName);
			$action->values($pmsData);
		}
		
		$sql = new Sql($this->dbAdapter);
		$stmt = $sql->prepareStatementForSqlObject($action);
		$result = $stmt->execute();

		
		if($result instanceof ResultInterface) {
			if($newId = $result->getGeneratedValue()){
				//when a value has been generated, set it on the object
				echo $pmsObject->setId($newId);
			}
			return $pmsObject;
		}
		
		throw new \Exception("Database Error");
	}
	
	/**
	* @return array/PmsRatings()
	* The following function is for listing data for select/dropdown form
	* For e.g. Need to fill the objectives field with Objectives from the database
	*/
	public function listSelectData($tableName, $columnName)
	{
		$sql = new Sql($this->dbAdapter);
		$select = $sql->select();

		$select->from(array('t1' => $tableName));
		$select->columns(array('id',$columnName)); 

		$stmt = $sql->prepareStatementForSqlObject($select);
		$result = $stmt->execute();
		
		$resultSet = new ResultSet();
		$resultSet->initialize($result);
		
		//Need to make the resultSet as an array
		// e.g. 1=> Objective 1, 2 => Objective etc.
		
		$selectData = array();
		foreach($resultSet as $set)
		{
			$selectData[$set['id']] = $set[$columnName];
		}
		return $selectData;
			
	}
        
}